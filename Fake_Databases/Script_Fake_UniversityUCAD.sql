CREATE PROCEDURE sp_SaveStudent
	@Names VARCHAR(100),
	@FirstSurname VARCHAR(100),
	@SecondSurname VARCHAR(100),
	@BirthDate DATE,
	@Age INT,
	@GenderId INT,
	@CarrersId INT,
	@MaximunLevelStudiesId INT,
	@CivilStatusId INT,
	@StudentStatusId INT
AS
BEGIN
	INSERT INTO 
		Students (Names,
		FirstSurname,
		SecondSurname,
		BirthDate,
		Age,
		GenderId,
		CarrersId,
		MaximunLevelStudiesId,
		CivilStatusId,
		StudentStatusId)
		VALUES (@Names,
		@FirstSurname,
		@SecondSurname,
		@BirthDate,
		@Age,
		@GenderId,
		@CarrersId,
		@MaximunLevelStudiesId,
		@CivilStatusId,
		@StudentStatusId)
END
GO

CREATE PROCEDURE sp_UpdateStudent
	@Id INT,
	@Names VARCHAR(100),
	@FirstSurname VARCHAR(100),
	@SecondSurname VARCHAR(100),
	@BirthDate DATE,
	@Age INT,
	@GenderId INT,
	@CarrersId INT,
	@MaximunLevelStudiesId INT,
	@CivilStatusId INT,
	@StudentStatusId INT
AS
BEGIN
	UPDATE Students 
	SET Names = @Names,
		FirstSurname = @FirstSurname,
		SecondSurname = @SecondSurname,
		BirthDate = @BirthDate,
		Age = @Age,
		GenderId = @GenderId,
		CarrersId = @CarrersId,
		MaximunLevelStudiesId = @MaximunLevelStudiesId,
		CivilStatusId = @CivilStatusId,
		StudentStatusId = @StudentStatusId
	WHERE Id = @Id
END
GO

CREATE PROCEDURE sp_DeleteStudent
	@Id INT
AS
BEGIN
	DELETE FROM Students WHERE Id = @Id
END
